    public boolean login(String un, String pw)
    {
        MysqlCon m = new MysqlCon();
        String p = new String();
        try
        {
            Statement stmt = m.con.createStatement();  
            ResultSet rs = stmt.executeQuery("select * from mydb.users where username = " + "BINARY '" + un + "'"); 
            rs.next();
            p = rs.getString(2);
            m.con.close();
        }
        catch(SQLException se){}

        return pw.equals(p) && !p.equals("");
    }
    
    public boolean usernameExists(String un)
    {
        MysqlCon m = new MysqlCon();
        boolean t = false;
        try
        {
            Statement stmt = m.con.createStatement();  
            ResultSet rs = stmt.executeQuery("select count(*) from mydb.users where username = " + "'" + un + "'"); 
            rs.next();
            t = rs.getInt(1) == 1;
            m.con.close();
        }
        catch(SQLException se){se.printStackTrace();}
        return t;
    }
    
