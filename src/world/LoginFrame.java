    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String un = jTextField1.getText();
        String pw = new String(jPasswordField1.getPassword());
        customer = new Customer(un);
        if(usernameExists(un))
        {
            if(customer.getOnline())
            {
                jLabel4.setVisible(true);
                jLabel4.setText("User already logged in");
                return;
            }
        }
        if(login(un,pw) && customer.isCustomer())
        {
            jLabel4.setVisible(true);
            jLabel4.setText("Logged in");
            this.setVisible(false);
            customer.fetchCustomer();
            customer.setOnline(1);
            new MapFrame(customer).setVisible(true);
            ui.setVisible(false);
        }
        else
        {
            jLabel4.setVisible(true);
            jLabel4.setText("Login failed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

